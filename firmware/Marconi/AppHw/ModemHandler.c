// USAGE:             handles all modem functions
//				
//
// INPUT:              N/A
//
// OUTPUT:           N/A
//
// GLOBALS:         N/A
//
//******************************************************************************
void ModemHandler(void)
{
	BYTE count;
	//
	// Fires all pipelines once
	//
	for (count = 0; count < NUM_OF_PIPELINES; count++)
	{
		if (PipelinesInstantiationList[count].PipelineSelect->optionsFlags 
			& PIPELINE_OPTIONS_RUN_IN_MAIN_LOOP)
		{
			PipelineFire(PipelinesInstantiationList[count].PipelineSelect);
		}
	}
}

