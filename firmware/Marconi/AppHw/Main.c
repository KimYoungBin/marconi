int main(void)
{
	HwInit();

	HwUartOpen(APP_UART_ID, 9600, HW_UART_PARITY_NONE);
	HwUartOpen(DEBUG_UART_ID, 115200, HW_UART_PARITY_NONE);
	HwCodecOpen(HW_CODEC_SAMPLE_RATE_8_KHZ);
	HwCodecSetRxChannel(HW_CODEC_RX_CHANNEL_2);

	HwTimerConfigure(1, IncrementTimeMS);
	HwTimerEnable();
	
	ModemInit();
//	ListAllControllersAction(NULL);
	ListAllPipelinesAction(NULL);
	DebugMsg("\n\n\rtype 'help' for a list of available commands");
	DisplayPrompt();
    debugModeEnabled = FALSE;

	while (TRUE)
	{
		DebugHandler();

		if (debugModeEnabled == FALSE)
		{
			LEDHandler();
			ModemHandler();
		}
	}
}


