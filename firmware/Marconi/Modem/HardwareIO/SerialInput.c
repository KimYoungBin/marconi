//******************************************************************************
//
//	Serial Input Execution Block
//
//******************************************************************************
WORD SerialInput(WORD *inBuffer, WORD *inSize, WORD *outBuffer, WORD *outSize, BYTE *BlockObjStruct)
{
	//
	// Copies data in the UART to the outBuffer
	// The output buffer is filled as much as possible from the UART
	//
	*outSize = HwUartRd(
				APP_UART_ID, 
				(char *)outBuffer, 
				*outSize
			);
	
	return 0;
}

//******************************************************************************
//
//	Serial Input Ready Check
//
//******************************************************************************
WORD SerialInputReady(WORD *inputSize, WORD *outputSize, BYTE *BlockObjStruct)
{
	if ((HwUartRdAvail(APP_UART_ID) >= 1 )&& (*outputSize >= 1))
	{
		*outputSize = MIN(HwUartRdAvail(APP_UART_ID), *outputSize);

		return TRUE;
	}
	return FALSE;
}

