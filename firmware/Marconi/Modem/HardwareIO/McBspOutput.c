//******************************************************************************
//
//	McBsp Output Execution Block
//
//******************************************************************************
WORD McBspOutput(WORD *inBuffer, WORD *inSize, WORD *outBuffer, WORD *outSize, BYTE *BlockObjStruct)
{
	*inSize = HwCodecWr(inBuffer, *inSize);
	
	return 0;
}

//******************************************************************************
//
//	McBsp Output Ready Check
//
//******************************************************************************
WORD McBspOutputReady(WORD *inputSize, WORD *outputSize, BYTE *BlockObjStruct)
{
	if (*inputSize >= 1 && HwCodecWrAvail() >= 1)
	{
		*inputSize = MIN(HwCodecWrAvail(), *inputSize);
		return TRUE;
	}
	return FALSE;
}

