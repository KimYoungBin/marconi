WORD PassThrough(WORD *inBuffer, WORD *inSize, WORD *outBuffer, WORD *outSize, BYTE *BlockObjStruct)
{
	*inSize = MIN(*inSize, *outSize);
	*outSize = *inSize;

	memcpy(outBuffer, inBuffer, *inSize * sizeof(WORD));
	return RETURN_OK;
}


WORD PassThroughReady(WORD *inputSize, WORD *outputSize, BYTE *BlockObjStruct)
{
	if (*inputSize > 0 && *outputSize > 0)
	{
		return TRUE;
	}
	return FALSE;
}


